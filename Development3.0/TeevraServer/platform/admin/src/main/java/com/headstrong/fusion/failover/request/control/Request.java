package com.headstrong.fusion.failover.request.control;

import org.jgroups.Message;

/**
 * Represents a request received by the Node on a 
 * JGroups Channel.
 * 
 */
public interface Request {
	public static byte MEMBER_REGISTRATION_REQUEST = 1;
	public static byte SELF_ACTIVATION_REQUEST = 2;
	public static byte MEMBER_INFO_REQUEST = 3;

	/**
	 * Returns Message object as expected by Channel message dispatcher.
	 * Converts the request ( as generated by the instance ) to a JGroup
	 * message so that it can be dispatched to other instances.
	 * 
	 * @return {@link Message}
	 * @throws Exception
	 */
	Message getMessage() throws Exception;

	/**
	 * Reads the JGroups Message and creates the corresponding request so 
	 * that it can be understood by the application which need not be
	 * aware of underlying JGroup infrastructure.
	 * 
	 * @param message {@link Message}
	 * @throws Exception
	 */
	void read(Message message) throws Exception;
}
